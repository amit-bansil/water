package cps.jarch.simulation.graphics;

import java.awt.Color;
import java.awt.Font;

/**
 * Defines how a GraphicsEx object should be created & configured.
 * Use an instance of GraphicsExFactory to actually create graphics objects from
 * a GraphicsConfiguration. Note that most properties can be null, which means the
 * factory will use the most logical possible default.<br>
 * @version $Id: GraphicsConfiguration.java 523 2005-08-30 21:31:54Z bansil $
 * @author Amit Bansil
 */
public interface GraphicsConfiguration {
	//TODO copy comments from full graphics model
	public Font getTitleFont();
	public Font getLabelFont();
	public Double getThinStrokeWidth();
	public Double getThickStrokeWidth();
	public Double getExtraThickStrokeWidth();
	public Double getStdStrokeWidth();
	public Double getSmallHandleSize();
	public Double getLargeHandleSize();
	public Color getBackgroundColor();
	public Color getForegroundColor();
	public Color getDisabledColor();
	public Color getActiveColor();
}
